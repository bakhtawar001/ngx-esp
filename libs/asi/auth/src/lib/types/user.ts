import { IUser } from '@cosmos/common';

export class User implements IUser {
  Id!: number;
  Name!: string;

  constructor(user: Partial<User>) {
    Object.assign(this, user);
  }

  // -----------------------------------------------------------------------------------------------------
  // @ Accessors
  // -----------------------------------------------------------------------------------------------------
  get displayName(): string {
    return this.Name;
  }

  // -----------------------------------------------------------------------------------------------------
  // @ Public methods
  // -----------------------------------------------------------------------------------------------------
}
